    if (data->ui_tree) {
        ui_tree_update(data->ui_tree, delta_time);
    }
    
    // NOTE: Les √©v√©nements SDL sont maintenant g√©r√©s dans la boucle principale
    // Ne pas faire SDL_PollEvent ici pour √©viter les conflits
