    printf("   üéÆ  Bouton Play (avec PNG background)\n");
    printf("   üö™  Bouton Quit (avec PNG background)\n");
    printf("   üìä  Z-index calcul√©s automatiquement\n");
    printf("   üîç  Logs d'√©v√©nements activ√©s\n");
    
